def add(a,b):
	c = a + b
	print(c)
def subtract(a,b):
	c = a - b
	print(c)
def multiply(a,b):
	c = a * b
	print(c)
def division(a,b):
	c = a//b
	print(c)
def modulus(a,b):
	c = a % b
	print(c)
def percentage(a,b):
	c = (a/b) * 100
	print(c)
def float(a,b):
	c = a/b
	print(c)
def exponent(a,b):
	c = a**b
	print(c)
	 